package College.Exercixes_list;

public class Q28 {

    //Gerar e escrever números ímpares lidos entre 100 e 200

    public static void main(String[] args) {
        for (int i = 100; i <= 200; i++) {
            if (i % 2 != 0) {
                System.out.println(i);
            }
        }
    }

    
}
